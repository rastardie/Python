from statistics import mean, median, mode, variance

#Mean
print(mean([1,2,2,2,1,3,4,1,5]))
print("-------------------------------")

#Meidan
print(median([1,2,2,2,1,3,4,1,5]))
print("-------------------------------")

#Mode
print(mode([1,2,2,2,1,3,4,1,2,5]))
print("-------------------------------")

#Variance
print(variance([1,2,2,2,1,3,4,1,2,5]))
print("-------------------------------")


